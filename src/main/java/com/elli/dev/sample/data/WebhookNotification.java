package com.elli.dev.sample.data;

/**
 * Message object received as request payload
 */
public class WebhookNotification {
    private String eventId;
    private String eventTime;
    private String eventType;
    private WebhookNotificationResource meta;

    public String getEventId() {
        return eventId;
    }

    public void setEventId(String eventId) {
        this.eventId = eventId;
    }

    public String getEventTime() {
        return eventTime;
    }

    public void setEventTime(String eventTime) {
        this.eventTime = eventTime;
    }

    public String getEventType() {
        return eventType;
    }

    public void setEventType(String eventType) {
        this.eventType = eventType;
    }

    public WebhookNotificationResource getmeta() {
        return meta;
    }

    public void setResourceData(WebhookNotificationResource meta) {
        this.meta = meta;
    }

}
